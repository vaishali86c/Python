# Integer
integer_var = 10

# Float
float_var = 3.14

# Complex
complex_var = 2 + 3j

# String
string_var = "Hello, world!"

# Bytes
bytes_var = b"bytes data"

# Bytearray
bytearray_var = bytearray(b"bytearray data")

# Boolean
bool_var = True

# List
list_var = [1, 2, 3, 4]

# Tuple
tuple_var = (1, 2, 3)

# Set
set_var = {1, 2, 3}

# Frozen set
frozen_set_var = frozenset({1, 2, 3})

# Dictionary
dict_var = {"key1": "value1", "key2": "value2"}

# NoneType
none_var = None

# Printing the data types and their examples.
print("Integer:", integer_var)
print("Float:", float_var)
print("Complex:", complex_var)
print("String:", string_var)
print("Bytes:", bytes_var)
print("Bytearray:", bytearray_var)
print("Boolean:", bool_var)
print("List:", list_var)
print("Tuple:", tuple_var)
print("Set:", set_var)
print("Frozen Set:", frozen_set_var)
print("Dictionary:", dict_var)
print("NoneType:", none_var)
